package com.example.recyclerviewwithcardview;

public class Model {

    private String header;
    private  String desc;
    private  int imagName;

    public String getHeader() {
        return header;
    }

    public void setHeader(String header) {
        this.header = header;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public int getImagName() {
        return imagName;
    }

    public void setImagName(int imagName) {
        this.imagName = imagName;
    }
}
